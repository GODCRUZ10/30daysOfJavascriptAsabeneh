//1 Iterate 0 to 10 using for loop, do the same using while and do while loop
//for loop

for (let q = 0; q <= 10; q++) {
  console.log(q);
}

//while loop
let s = 0;
while (s <= 10) {
  console.log(s);
  s++;
}

//do while loop
let o = 0;
do {
  console.log(o);
  o++;
} while (o <= 10);

//2 Iterate 10 to 0 using for loop, do the same using while and do while loop

//for loop
for (let w = 10; w >= 0; w--) {
  console.log(w);
}
//while loop
let e = 10;
while (e >= 0) {
  console.log(e);
  e--;
}

//do while loop
let y = 10;
do {
  console.log(y);
  y--;
} while (y >= 0);

